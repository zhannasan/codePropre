package ex1;

/**
 * Classe qui réalise deux opérations arythmétiques
 * @author DIGINAMIC
 */
public class Calcul {
	/** Méthode qui retourne l'addition des paramètres a et b 
	 * 
	 * @param a paramètre a
	 * @param b paramètre b
	 * @return int 
	 */
	public static int addition(int a, int b){
		return a+b;
	}
	
	/** Méthode qui retourne la soustraction des paramètres a et b 
	 * 
	 * @param a paramètre a
	 * @param b paramètre b
	 * @return int
	 */
	public static int soustraction(int a, int b){
		return a-b;
	}
}
